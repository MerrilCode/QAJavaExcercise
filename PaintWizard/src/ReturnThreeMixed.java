public class ReturnThreeMixed {

    private final double first;
    private final double second;
    private final String third;

    public ReturnThreeMixed(double first, double second,String third){
        this.first = first;
        this.second = second;
        this.third = third;
    }

    public double getFirst() {
        return first;
    }

    public double getSecond() {
        return second;
    }

    public String getThird() {
        return third;
    }
}
